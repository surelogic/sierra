package com.surelogic.sierra.jdbc.finding;

public class FindingGenerationException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -5558171214177305062L;

	public FindingGenerationException() {
	}

	public FindingGenerationException(String message) {
		super(message);
	}

	public FindingGenerationException(Throwable cause) {
		super(cause);
	}

	public FindingGenerationException(String message, Throwable cause) {
		super(message, cause);
	}

}
