CREATE TABLE FILTER_ENTRY (
  FILTER_SET_ID   BIGINT NOT NULL CONSTRAINT FE_FILTER_SET_FK REFERENCES FILTER_SET (ID) ON DELETE CASCADE,
  FINDING_TYPE_ID BIGINT NOT NULL CONSTRAINT FE_FINDING_TYPE_FK REFERENCES FINDING_TYPE (ID),
  FILTERED        CHAR(1) NOT NULL CONSTRAINT FE_FILTERED_CN (CHECK (FILTERED IN ('Y','N')),
  PRIMARY KEY (FILTER_SET_ID,FINDING_TYPE_ID)
);

