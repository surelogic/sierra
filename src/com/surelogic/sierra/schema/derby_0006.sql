CREATE TABLE FILTER_SET_RELTN (
  CHILD_ID  BIGINT NOT NULL CONSTRAINT CHILD_FILTER_SET_FK REFERENCES FILTER_SET (ID),
  PARENT_ID BIGINT NOT NULL CONSTRAINT PARENT_FILTER_SET_FK REFERENCES FILTER_SET (ID),
  PRIMARY KEY (CHILD_ID,PARENT_ID)
)

