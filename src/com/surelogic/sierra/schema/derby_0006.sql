
CREATE TABLE FILTER_SET (
  ID   BIGINT         NOT NULL PRIMARY KEY,
  UUID CHAR(36)       UNIQUE NOT NULL,
  NAME VARCHAR(32672) NOT NULL,
  INFO VARCHAR(32672)
)

